package com.opengleswagonwheel;

public class OnNewSurfaceSizeSet {

	private int width;
	private float newHeight;

	public int getWidth() {
		return width;
	}

	public float getNewHeight() {
		return newHeight;
	}

	public OnNewSurfaceSizeSet(int width, float newHeight) {
		this.width = width;
		this.newHeight = newHeight;
	}

}
